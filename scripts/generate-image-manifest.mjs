import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");

const imageRoot = path.join(rootDir, "images");
const outputFile = path.join(rootDir, "src", "imageManifest.ts");

const categories = [
  { key: "finishes", folder: "finishes" },
  { key: "works", folder: "works" }
];

const allowedExtensions = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif"]);

function toPosix(input) {
  return input.replace(/\\/g, "/");
}

async function getImageFiles(folderPath) {
  const entries = await fs.readdir(folderPath, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((name) => allowedExtensions.has(path.extname(name).toLowerCase()))
    .sort((a, b) => a.localeCompare(b));
}

async function main() {
  const manifest = {};

  for (const category of categories) {
    const folderPath = path.join(imageRoot, category.folder);
    const files = await getImageFiles(folderPath);
    manifest[category.key] = files.map((file) => toPosix(`/${category.folder}/${file}`));
  }

  const output = `// Auto-generated by scripts/generate-image-manifest.mjs\n// Do not edit manually.\n\n` +
    `export type ImageManifest = {\n  finishes: string[];\n  works: string[];\n};\n\n` +
    `export const imageManifest: ImageManifest = ${JSON.stringify(manifest, null, 2)};\n`;

  await fs.writeFile(outputFile, output, "utf8");
  console.log(`Image manifest updated: ${path.relative(rootDir, outputFile)}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
